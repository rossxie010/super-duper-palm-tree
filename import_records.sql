-- Count records in each table
SELECT 'Users' as Table_Name, COUNT(*) as Record_Count FROM Users
UNION ALL
SELECT 'Stocks', COUNT(*) FROM Stocks
UNION ALL
SELECT 'Portfolios', COUNT(*) FROM Portfolios
UNION ALL
SELECT 'Transactions', COUNT(*) FROM Transactions
UNION ALL
SELECT 'Stock_Price_History', COUNT(*) FROM Stock_Price_History
UNION ALL
SELECT 'Watchlists', COUNT(*) FROM Watchlists
UNION ALL
SELECT 'Watchlist_Items', COUNT(*) FROM Watchlist_Items
ORDER BY Table_Name;

-- Sample portfolio summary
SELECT 
    p.portfolio_id,
    p.name as portfolio_name,
    u.username,
    COUNT(DISTINCT t.transaction_id) as total_transactions,
    SUM(CASE WHEN t.type = 'BUY' THEN t.quantity * t.price ELSE 0 END) as total_invested,
    SUM(CASE WHEN t.type = 'SELL' THEN t.quantity * t.price ELSE 0 END) as total_sold
FROM Portfolios p
JOIN Users u ON p.user_id = u.user_id
LEFT JOIN Transactions t ON p.portfolio_id = t.portfolio_id
GROUP BY p.portfolio_id, p.name, u.username
ORDER BY p.portfolio_id
LIMIT 10;

-- Most active stocks
SELECT 
    s.symbol,
    s.company_name,
    COUNT(t.transaction_id) as transaction_count,
    SUM(t.quantity) as total_volume
FROM Stocks s
JOIN Transactions t ON s.stock_id = t.stock_id
GROUP BY s.symbol, s.company_name
ORDER BY transaction_count DESC
LIMIT 10;

-- Watchlist statistics
SELECT 
    w.watchlist_id,
    u.username,
    w.name as watchlist_name,
    COUNT(wi.item_id) as stock_count
FROM Watchlists w
JOIN Users u ON w.user_id = u.user_id
LEFT JOIN Watchlist_Items wi ON w.watchlist_id = wi.watchlist_id
GROUP BY w.watchlist_id, u.username, w.name
ORDER BY w.watchlist_id;

-- Recent price data sample
SELECT 
    s.symbol,
    sph.price,
    sph.timestamp
FROM Stock_Price_History sph
JOIN Stocks s ON sph.stock_id = s.stock_id
WHERE s.symbol IN ('AAPL', 'MSFT', 'GOOGL')
ORDER BY sph.timestamp DESC
LIMIT 15;

-- Transaction summary by user
SELECT 
    u.username,
    COUNT(t.transaction_id) as total_transactions,
    SUM(CASE WHEN t.type = 'BUY' THEN 1 ELSE 0 END) as buy_count,
    SUM(CASE WHEN t.type = 'SELL' THEN 1 ELSE 0 END) as sell_count,
    SUM(t.quantity) as total_volume
FROM Users u
JOIN Portfolios p ON u.user_id = p.user_id
JOIN Transactions t ON p.portfolio_id = t.portfolio_id
GROUP BY u.username
ORDER BY total_transactions DESC;

